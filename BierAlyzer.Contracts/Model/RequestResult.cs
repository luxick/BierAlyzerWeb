using ProtoBuf;

namespace BierAlyzer.Contracts.Model
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Encapsulates the result of a request. </summary>
    /// <remarks>   Andre Beging, 18.11.2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    [ProtoContract]
    public class RequestResult
    {
        #region Constructors

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Default constructor. </summary>
        /// <remarks>   Andre Beging, 18.11.2018. </remarks>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public RequestResult() {}

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        /// <remarks>   Andre Beging, 18.11.2018. </remarks>
        /// <param name="status">       The status. </param>
        /// <param name="exception">    The exception. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public RequestResult(RequestResultStatus status, string exception)
        {
            Status = status;
            Exception = exception;
        }

        #endregion

        /// <summary>   Indicates if the request status is success. </summary>
        [ProtoMember(10)]
        public bool Success => Status == RequestResultStatus.Success;

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Indicates the request status. </summary>
        /// <value> The status. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        [ProtoMember(20)]
        public RequestResultStatus Status { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   The exception if the an exception occured </summary>
        /// <value> The exception. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        [ProtoMember(30)]
        public string Exception { get; set; }


    }
}
